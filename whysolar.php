	<h4><blockquote> How much Electricity do Dirty Solar Panels Lose?</h4>
		<div class="text">
		
		Pollution, traffic dust, leaves and even bird droppings contribute to preventing sunlight from reaching the solar cells in your solar panels.<br><br>
The more dirt – the lower amount of electricity they will produce. While many factors affect how much electricity your solar panels will produce – dirty solar panels can be one of the biggest, and easiest to fix.<br><br>
Experts agree that dirty solar panels don't produce as much power as clean panels. That loss may range as high as 25% in some areas according to the National Renewable Energy laboratory. Individual dealers have reported losses as high as 30% for some customers who failed to ever clean their panels.<br><br>

		</div>
		
		<h4><blockquote> Two Ways To Check Your Panels</h4>
		<h6>There are two ways you can check to see if your panels need to be cleaned:</h6>
		<div class="text">
		
		
1. <U>Physical Inspection:</U>  &nbsp;&nbsp;&nbsp; Inspect the solar panels on a periodic basis (frequency depends on location or the manufacturer's specifications) 
to remove any debris and dirt and ensure all connections are tight. Generally, speaking dusty areas typically require more frequent inspection.<br><br>

		</div>
		
		<div class="text">
		2. <U>Use a Monitoring Service:</U> &nbsp;&nbsp;&nbsp; Another way to make sure you’re getting the most solar potential out of your PV system is through a monitoring systems and service. For a monthly fee or a flat up front cost, you can monitor how your system is performing.
Your system is expected to produce a certain amount of power during each month. A solar monitoring system can tell you if your system is off line or if it’s not performing as expected and run diagnostic programs.
Solar monitoring systems can also be educational, showing you how much power you’ve saved, how much CO2 you’ve saved, and how much money you’ve saved.<br>
Depending on the monitoring systems, you can access your information from the web, from a wall mounted-device, or even from your iPhone or other mobile device.<br>
Monitoring systems are usually an extra expense, whether as a monthly service or purchased up front. While not essential, they do make troubleshooting and system performance easier to see.<br><br>
