<h5><blockquote>Is It Worth It To Clean Your Solar Panels?</h5>
<div class="text">
<p>Based on the research, experts are leaning toward “no.” And especially if you want to hire a professional company to clean them over the DIY method. The return on investment is just not there to justify the trouble. Let’s take a look at some numbers. On average, when your solar panels get dirty, expect a 5% or less change in output. And that’s just when they’re dirty. In most climates, if there is dust or debris on the panels and their energy output is registered 5% less than other times, the next rain will wash away the debris and the solar panels will return to their optimal efficiency. Even if you live in a drought-prone area – it will rain again.<br><br>
In addition, most solar panels are tilted to a high enough degree that most buildup will run right off the panel instead of sticking to the top of it. Even with tilted panels, you might have some slight buildup on the lowest panel (because the runoff collects at the bottom) – but the decrease in output based on this alone is so minuscule, it’s not something worth worrying about.<br>
Lastly, it’s a pretty small tradeoff, energy-wise and cost-wise, to climb up on a ladder, get on your roof, and clean your solar panels yourselves. When you think about getting out there and dragging a hose up to the roof to clean them, this point might really start to hit home.
</p>
